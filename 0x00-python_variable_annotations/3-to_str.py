#!/usr/bin/env python3

"""Contains a type annoted function, to_str,
    that returns the string representation of
    a float argument
"""


def to_str(n: float) -> str:
    """Returns the string representation of n

        Parameters
        ----------
        n (float): Float number

        Returns
        -------
        (str): The string representation of n
    """
    return str(n)
