#!/usr/bin/env python3

"""Contains a type annoted function, add, that sums two float arguments
"""

def add(a: float, b: float) -> float:
    """Adds two float numbers
    
        Parameters
        ----------
        a: float
            A float number
        b: float
            A float number
    
        Returns
        -------
        float
            The sum of a and b float arguments
    """
    return a + b;
