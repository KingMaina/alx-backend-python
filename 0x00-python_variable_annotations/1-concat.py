#!/usr/bin/env python3

"""Contains a type annoted function, concat,
    that concatinates two string arguments
"""


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings

        Parameters
        ----------
        str1 (str): The first string
        str2 (str): The second string

        Returns
        -------
        (str):  A concatenated string made from the string arguents
    """
    return str1 + str2
